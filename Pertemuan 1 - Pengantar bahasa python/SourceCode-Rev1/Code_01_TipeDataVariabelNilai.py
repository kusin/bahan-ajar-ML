# main method
if __name__ == "__main__":

  # Jenis-Jenis Tipe Data
  # 1. Bilangan Bulat = integer
  nilai = 5
  print("Nilai :",nilai)

  # 2. Bilangan pecahan = double
  nilai = 5.5
  print("Nilai :",nilai)

  # 3. Lebih satu karakter = String
  nama = "Aryajaya Alamsyah"
  print("Nama :",nama)

  # 4. Hanya satu karaketer
  nilai = 'A'
  print("Nilai :",nilai)

  # 5. Logika
  bool = True
  print("Nilai logika :",bool)
  bool = False
  print("Nilai logika :",bool)