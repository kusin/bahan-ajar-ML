# main method
if __name__ == "__main__":

  # # 1. Operator Aritmatika
  # tambah, kurang, kali, bagi
  a = 5
  b = 2

  # tambah
  c = a + b
  print("Hasil penjumlahan :", c)

  # kurang
  c = a - b
  print("Hasil pengurangan :", c)

  # bagi
  c = a / b
  print("Hasil pembagian :", c)

  # kali
  c = a * b
  print("Hasil perkalian :", c)

  # 2. Operator Perbandingan
  a = 5
  b = 2

  c = a == b
  print("Operator == :", c)

  c = a != b
  print("Operator != :", c)

  c = a >= b
  print("Operator >=:", c)

  c = a <= b
  print("Operator <=:", c)